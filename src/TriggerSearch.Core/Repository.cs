        public void Modified(TEntity entity, params string[] fields)
        {
            _dbSet.Attach(entity);
            _dbContext.Entry(entity).State = EntityState.Modified;
            if(fields.Length > 0)
            {
                 var peroperties = _dbContext.Entry(entity).Properties;
                foreach (var property in peroperties)
                {
                    if(fields.Contains(property.Metadata.Name))
                        property.IsModified = true;
                    else
                        property.IsModified = false;
                }
                
            }
            
        }
